let dataPorPartido = () => {
  let res = {};
  for (const deputado of dataDeputados) {
    if (deputado.votou !== 'SIM' && deputado.votou !== 'NÃO') {
    } else if (!res[deputado.partido]) {
      res[deputado.partido] = {
        TOTAL: 1,
        ADESAO: deputado.votou === 'SIM' ? 1 : 0,
        SIM: deputado.votou === 'SIM' ? 1 : 0,
        NAO: deputado.votou === 'NÃO' ? 1 : 0,
        OUTRO: deputado.votou !== 'SIM' && deputado.votou !== 'NÃO' ? 1 : 0
      };
    } else {
      let record = res[deputado.partido];
      res[deputado.partido] = {
        TOTAL: record.TOTAL + 1,
        ADESAO:
          deputado.votou === 'SIM'
            ? (record.SIM + 1) / (record.TOTAL + 1)
            : record.SIM / (record.TOTAL + 1),
        SIM: deputado.votou === 'SIM' ? record.SIM + 1 : record.SIM,
        NAO: deputado.votou === 'NÃO' ? record.NAO + 1 : record.NAO,
        OUTRO:
          deputado.votou !== 'SIM' && deputado.votou !== 'NÃO'
            ? record.OUTRO + 1
            : record.OUTRO
      };
    }
  }
  res = Object.keys(res).map((key) => ({
    ...res[key],
    partido: key
  }));
  return res;
};
